    private Cliente carregarCliente() {
        Cliente cliente = new Cliente();
        cliente.setNome(nome.getText());
        cliente.setEmail(email.getText());
        cliente.setTelefone(Integer.parseInt(telefone.getText()));
        cliente.setObservacao(observacao.getText());
        if ("".equals(cod_cliente.getText())) {
            cliente.setCod_cliente(0);
        } else {
            cliente.setCod_cliente(Integer.parseInt(cod_cliente.getText()));
        }

        return cliente;
    }
    
