    private void bGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bGravarActionPerformed

        Cliente cliente = carregarCliente();

        if (cliente.getCod_cliente()> 0) {
            try {
                int clienteAtualizado = ClienteDAO.atualizar(cliente);
                if (clienteAtualizado > 0) {
                    JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso!");
                }

            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        } else {
            try {
                int clienteInserido = ClienteDAO.inserir(cliente);
                if (clienteInserido > 0) {
                    JOptionPane.showMessageDialog(null, "Cliente inserido com sucesso!");
                }
            } catch (SQLException | ClassNotFoundException ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }
    }//GEN-LAST:event_bGravarActionPerformed

